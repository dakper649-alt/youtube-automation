def get_recommended_voices(niche: str) -> list:
    """
    –ü–æ–ª—É—á–∏—Ç—å —Ä–µ–∫–æ–º–µ–Ω–¥–æ–≤–∞–Ω–Ω—ã–µ –≥–æ–ª–æ—Å–∞ –¥–ª—è –Ω–∏—à–∏

    Args:
        niche: –ù–∏—à–∞ –∫–æ–Ω—Ç–µ–Ω—Ç–∞ (psychology, business, storytelling –∏ —Ç.–¥.)

    Returns:
        –°–ø–∏—Å–æ–∫ —Ä–µ–∫–æ–º–µ–Ω–¥–æ–≤–∞–Ω–Ω—ã—Ö –≥–æ–ª–æ—Å–æ–≤ —Å –º–µ—Ç–∞–¥–∞–Ω–Ω—ã–º–∏
    """
    recommended = []
    for key, voice in ELEVENLABS_VOICES.items():
        if niche in voice['niches']:
            rec = {
                'key': key,
                'name': voice['name'],
                'emoji': voice['emoji'],
                'description': voice['description'],
                'is_recommended': voice.get('recommended', False)
            }
            recommended.append(rec)

    # –°–æ—Ä—Ç–∏—Ä—É–µ–º: —Ä–µ–∫–æ–º–µ–Ω–¥–æ–≤–∞–Ω–Ω—ã–µ –ø–µ—Ä–≤—ã–º–∏
    recommended.sort(key=lambda x: (not x['is_recommended'], x['name']))
    return recommended


